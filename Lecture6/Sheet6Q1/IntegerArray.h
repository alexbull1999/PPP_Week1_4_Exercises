/* This header file declares four functions. */

/* Function input_array(a , n) allows the user to input values for the first n elements of the array a*/

void input_array(int a[], int n);

void input_array(char a[], int n);

/* Function display_array(a, n) displays the values of the first n elements of the array a on the screen */

void display_array(int a[], int n);

void display_array(char a[], int n);

/* Function which copies the first n elements of a2 to the respective first n elements in a1 */

void copy_array(char a1[], char a2[], int n);

/* Function which returns the standard deviation of the first n elements of a */

double average(int a[], int n);

double average(double a[], int n);

void standard_deviation(int a[], int n);



